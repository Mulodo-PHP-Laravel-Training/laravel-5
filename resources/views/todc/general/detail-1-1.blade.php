<h3>Server Requirement</h3>
<p>The Laravel framework has a few system requirements. Of course, all of these requirements are satisfied by the Laravel Homestead virtual machine:</p>
<ul>
<li>PHP >= 5.5.9
<li>OpenSSL PHP Extension
<li>PDO PHP Extension
<li>Mbstring PHP Extension
<li>Tokenizer PHP Extension
</ul>